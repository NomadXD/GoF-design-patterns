package Builder;

public class Pizza {
    private String dough = "";
    private String sauce = "";
    private String top = "";

    public void setDough(String dough) {
        this.dough = dough;
    }

    public void setSauce(String sauce) {
        this.sauce = sauce;
    }

    public void setTop(String top) {
        this.top = top;
    }
}
